package ReviewClass9.OverLoadingDemo;

public class OverLoading {
    public static void main(String[] args) {

        String name="yovanna is a good student, yovanna does her homework on time"; //starting from 0, no restriction
        System.out.println(name.indexOf("yovanna"));  //built in overloading
        System.out.println(name.indexOf("yovanna",10));

    }
}
